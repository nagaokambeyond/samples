DROP TABLE IF EXISTS book;
DROP TABLE IF EXISTS book_genre;
DROP TABLE IF EXISTS publisher;

CREATE TABLE publisher (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    publisher_name VARCHAR(100) NOT NULL,
    create_at TIMESTAMP NOT NULL,
    update_at TIMESTAMP NOT NULL,
    version BIGINT NOT NULL
);

comment on table publisher is '出版社';
comment on column publisher.publisher_name is '出版社名';
comment on column publisher.create_at is '作成日時';
comment on column publisher.update_at is '更新日時';
comment on column publisher.version is 'バージョン';

CREATE TABLE book_genre (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    genre_name VARCHAR(100) NOT NULL,
    create_at TIMESTAMP NOT NULL,
    update_at TIMESTAMP NOT NULL,
    version BIGINT NOT NULL
);

comment on table book_genre is '本ジャンル';
comment on column book_genre.genre_name is 'ジャンル名';
comment on column book_genre.create_at is '作成日時';
comment on column book_genre.update_at is '更新日時';
comment on column book_genre.version is 'バージョン';

CREATE TABLE book (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(100) NOT NULL,
    author VARCHAR(200),
    release_date DATE NOT NULL,
    publisher_id BIGINT NOT NULL,
    genre_id BIGINT NOT NULL,
    create_at TIMESTAMP NOT NULL,
    update_at TIMESTAMP NOT NULL,
    version BIGINT NOT NULL,
    FOREIGN KEY (publisher_id) REFERENCES publisher(id),
    FOREIGN KEY (genre_id) REFERENCES book_genre(id)
);

CREATE INDEX idx_book_01 ON book(release_date);
CREATE INDEX idx_book_02 ON book(publisher_id);
CREATE INDEX idx_book_03 ON book(genre_id);

